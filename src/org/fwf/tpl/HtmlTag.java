    public static final String address = "address"; // Defines contact information for the author/owner of a document
    public static final String applet = "applet"; // Not supported in HTML5. Use --embed-- or --object-- instead. Defines an embedded applet
    public static final String area = "area"; // Defines an area inside an image-map
    public static final String article = "article"; // Defines an article
    public static final String aside = "aside"; // Defines content aside from the page content
    public static final String audio = "audio"; // Defines sound content
    public static final String b = "b"; // Defines bold text
    public static final String base = "base"; // Specifies the base URL/target for all relative URLs in a document
    public static final String basefont = "basefont"; // Not supported in HTML5. Use CSS instead. Specifies a default color, size, and font for all text in a document
