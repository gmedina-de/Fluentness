    public static final String br = "br"; // Defines a single line break
    public static final String button = "button"; // Defines a clickable button
    public static final String canvas = "canvas"; // Used to draw graphics, on the fly, via scripting (usually JavaScript)
    public static final String caption = "caption"; // Defines a table caption
    public static final String center = "center"; // Not supported in HTML5. Use CSS instead. Defines centered text
    public static final String cite = "cite"; // Defines the title of a work
    public static final String code = "code"; // Defines a piece of computer code
