    public static final String s = "s"; // Defines text that is no longer correct
    public static final String samp = "samp"; // Defines sample output from a computer program
    public static final String script = "script"; // Defines a client-side script
    public static final String section = "section"; // Defines a section in a document
    public static final String select = "select"; // Defines a drop-down list
    public static final String small = "small"; // Defines smaller text
    public static final String source = "source"; // Defines multiple media resources for media elements (--video-- and --audio--)
    public static final String span = "span"; // Defines a section in a document
    public static final String strike = "strike"; // Not supported in HTML5. Use --del-- or --s-- instead. Defines strikethrough text
    public static final String strong = "strong"; // Defines important text
    public static final String style = "style"; // Defines style information for a document
    public static final String sub = "sub"; // Defines subscripted text
