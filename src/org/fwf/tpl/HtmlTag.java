    public static final String td = "td"; // Defines a cell in a table
    public static final String template = "template"; // Defines a template
    public static final String textarea = "textarea"; // Defines a multiline input control (text area)
    public static final String tfoot = "tfoot"; // Groups the footer content in a table
    public static final String th = "th"; // Defines a header cell in a table
    public static final String thead = "thead"; // Groups the header content in a table
    public static final String time = "time"; // Defines a date/time
    public static final String title = "title"; // Defines a title for the document
    public static final String tr = "tr"; // Defines a row in a table
    public static final String track = "track"; // Defines text tracks for media elements (--video-- and --audio--)
    public static final String tt = "tt"; // Not supported in HTML5. Use CSS instead. Defines teletype text
    public static final String u = "u"; // Defines text that should be stylistically different from normal text
    public static final String ul = "ul"; // Defines an unordered list
    public static final String var = "var"; // Defines a variable
    public static final String video = "video"; // Defines a video or movie
    public static final String wbr = "wbr"; // Defines a possible line-break
