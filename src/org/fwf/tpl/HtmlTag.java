    public static final String dd = "dd"; // Defines a description/value of a term in a description list
    public static final String del = "del"; // Defines text that has been deleted from a document
    public static final String details = "details"; // Defines additional details that the user can view or hide
    public static final String dfn = "dfn"; // Represents the defining instance of a term
    public static final String dialog = "dialog"; // Defines a dialog box or window
    public static final String dir = "dir"; // Not supported in HTML5. Use --ul-- instead. Defines a directory list
    public static final String div = "div"; // Defines a section in a document
    public static final String dl = "dl"; // Defines a description list
    public static final String dt = "dt"; // Defines a term/name in a description list
    public static final String em = "em"; // Defines emphasized text
    public static final String embed = "embed"; // Defines a container for an external (non-HTML) application
    public static final String fieldset = "fieldset"; // Groups related elements in a form
    public static final String figcaption = "figcaption"; // Defines a caption for a --figure-- element
    public static final String figure = "figure"; // Specifies self-contained content
    public static final String font = "font"; // Not supported in HTML5. Use CSS instead. Defines font, color, and size for text
    public static final String footer = "footer"; // Defines a footer for a document or section
    public static final String form = "form"; // Defines an HTML form for user input
    public static final String frame = "frame"; // Not supported in HTML5. Defines a window (a frame) in a frameset
    public static final String frameset = "frameset"; // Not supported in HTML5. Defines a set of frames
    public static final String h1 = "h1"; // to public static final String h6 = "h6"; // Defines HTML headings
    public static final String head = "head"; // Defines information about the document
    public static final String header = "header"; // Defines a header for a document or section
    public static final String hr = "hr"; // Defines a thematic change in the content
    public static final String html = "html"; // Defines the root of an HTML document
    public static final String i = "i"; // Defines a part of text in an alternate voice or mood
    public static final String iframe = "iframe"; // Defines an inline frame
    public static final String img = "img"; // Defines an image
    public static final String input = "input"; // Defines an input control
    public static final String ins = "ins"; // Defines a text that has been inserted into a document
    public static final String kbd = "kbd"; // Defines keyboard input
    public static final String label = "label"; // Defines a label for an --input-- element
    public static final String legend = "legend"; // Defines a caption for a --fieldset-- element
    public static final String li = "li"; // Defines a list item
    public static final String link = "link"; // Defines the relationship between a document and an external resource (most used to link to style sheets)
    public static final String main = "main"; // Specifies the main content of a document
    public static final String map = "map"; // Defines a client-side image-map
    public static final String mark = "mark"; // Defines marked/highlighted text
    public static final String meta = "meta"; // Defines metadata about an HTML document
    public static final String meter = "meter"; // Defines a scalar measurement within a known range (a gauge)
    public static final String nav = "nav"; // Defines navigation links
    public static final String noframes = "noframes"; // Not supported in HTML5. Defines an alternate content for users that do not support frames
    public static final String noscript = "noscript"; // Defines an alternate content for users that do not support client-side scripts
    public static final String object = "object"; // Defines an embedded object
    public static final String ol = "ol"; // Defines an ordered list
    public static final String optgroup = "optgroup"; // Defines a group of related options in a drop-down list
    public static final String option = "option"; // Defines an option in a drop-down list
    public static final String output = "output"; // Defines the result of a calculation
    public static final String p = "p"; // Defines a paragraph
    public static final String param = "param"; // Defines a parameter for an object
    public static final String picture = "picture"; // Defines a container for multiple image resources
    public static final String pre = "pre"; // Defines preformatted text
    public static final String progress = "progress"; // Represents the progress of a task
    public static final String q = "q"; // Defines a short quotation
    public static final String rp = "rp"; // Defines what to show in browsers that do not support ruby annotations
